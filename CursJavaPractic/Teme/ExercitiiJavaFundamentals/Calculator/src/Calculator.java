public class Calculator {


    public int adunare(int x,int y) {
        return x + y;
    }

    public int scadere(int x,int y) {
        return x - y;
    }

    public int inmultire(int x,int y) {
        return x * y;
    }

    public int impartire(int x,int y) {
        return x / y;
    }
}
