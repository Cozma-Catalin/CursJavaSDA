public class CalculatorStiintific extends Calculator {


    public double ridicareLaPutere(double x, double y) {
        return Math.pow(x, y);
    }

    public double radical(double x) {
        return Math.sqrt(x);
    }

    public double logaritmBaza10(double x) {
        return Math.log10(x);
    }

    public double logaritm(double x) {
        return Math.log(x);
    }
}
